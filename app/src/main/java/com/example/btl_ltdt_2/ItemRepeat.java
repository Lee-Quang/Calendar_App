package com.example.btl_ltdt_2;

public class ItemRepeat {
    private String name;
    private int num;

    public ItemRepeat(String name, int num) {
        this.name = name;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
